#!/usr/bin/env python3

a = (1)
print('a = (1)')
print('type(a) =', type(a), '\n')

a = (1,)
print('a = (1,)')
print('type(a) =', type(a), '\n')

a = [1]
print('a = [1]')
print('type(a) =', type(a), '\n')

a = {'name':'Yoonghm', 'country':'Singapore'}
print("a = {'name':'Yoonghm', 'country':'Singapore'}")
print('type(a) =', type(a), '\n')
